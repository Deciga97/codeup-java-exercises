public class ControlFlowExercises {
    public static void main(String[] args) {
        int i = 5;
        while(i <= 15) {
            System.out.println(i);
            i++;
        }

        int x = 0;
        do{
            x += 2;
            System.out.println(x);
        } while(x <= 100);

    }
}
